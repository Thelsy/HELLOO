# 变量
# 每一个变量都存储了相对应的值，这个变量中的值是可变的，始终会记录最新的值
message = "Hello Word"
print(message)
print("---------------------------------------------------------------------------------------")
message = "Hello Word Django"
print(message)
'''
变量的命名与使用规范
    变量名只能包含字母，数字和下划线，变量名可以字母或者下划线打头，但是不能以数字开头
    变量名不可以包含空格，但可以使用下划线来分隔
    变量名不可以使用 python中的关键字和函数名用作变量名，也不可以使用保留字
'''
